"""
Write a Python program to import the math module and use functions like sqrt(), ceil(), 
floor().
"""
import math

squareRoot=math.sqrt(22)
print(f"Square root of 22 is : {squareRoot}")

num=14.22
ceilValue=math.ceil(num)
print(f"The ceil value of 14.22 is : {ceilValue}")

floorValue=math.floor(num)
print(f"The foor value of 14.22 is {floorValue}")